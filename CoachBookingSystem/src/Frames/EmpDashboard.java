    private void btnSearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSearchActionPerformed
        // TODO add your handling code here:
        if(txtPrice2.getText().length()>0)
        {
            try{
                if(searchString.length()>0)
                    searchString = searchString + " AND price<="+Double.parseDouble(txtPrice2.getText());
                else
                    searchString = searchString + " where price<="+Double.parseDouble(txtPrice2.getText());
            }
            catch(NumberFormatException e)
            {
                JOptionPane.showMessageDialog(rootPane, "Price field accept only number, Please check your value", "Incorrect Input", 
                        JOptionPane.ERROR_MESSAGE);
            }
            emp.loadAllRoute(table,panelModify,panelTable,labelID,comboDeptCountry1,comboDeptTown1,comboDestCountry1,
                    comboDestTown1,spinHour1,spinMinute1,spinAmPm1,txtPrice1,comboWeek);
        }
        else
        {
            emp.loadAllRoute(table,panelModify,panelTable,labelID,comboDeptCountry1,comboDeptTown1,comboDestCountry1,
                    comboDestTown1,spinHour1,spinMinute1,spinAmPm1,txtPrice1,comboWeek);
        }
        searchString="";
        comboDeptCountry2.setSelectedIndex(0);
        comboDestCountry2.removeAllItems();
        comboDestCountry2.addItem(" -dest country- ");
        comboDestCountry2.setSelectedIndex(0);
        comboDestCountry2.addItem("France");
        comboDestCountry2.addItem("Germany");
        comboDestCountry2.addItem("spain");
        txtPrice2.setText("");
        comboWeek1.setSelectedIndex(0);
    }//GEN-LAST:event_btnSearchActionPerformed

