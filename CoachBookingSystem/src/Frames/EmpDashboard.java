    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        // TODO add your handling code here:
        String errorMsg="";
        
        String deptCountry = comboDeptCountry.getSelectedItem().toString();
        String deptTown = comboDeptTown.getSelectedItem().toString();
        String destCountry = comboDestCountry.getSelectedItem().toString();
        String destTown = comboDestTown.getSelectedItem().toString();
        String hour = spinHour.getValue().toString();
        String minute = spinMinute.getValue().toString();
        String amPM = spinAmPm.getValue().toString();
        String deptTime = hour + ":" + minute + " " + amPM;
        double price=0;
        try {
            price = Double.parseDouble((txtPrice.getText()));
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(rootPane, "Price field accept only number, Please check your value", "Incorrect Input", 
                    JOptionPane.ERROR_MESSAGE);
        }

//        for(int j=0;j<noOfRecord;j++)
//        {
//            System.out.println(selectedWeek[j]+"\n");
//        }
        if(!(comboDeptCountry.getSelectedIndex()==0))
        {
            if(!(comboDeptTown.getSelectedIndex()==0))
            {
                if(!(comboDestCountry.getSelectedIndex()==0))
                {
                    if(!(comboDestTown.getSelectedIndex()==0))
                    {
                        if(!(noOfRecord==0))
                        {
                            for(int i=0;i<noOfRecord;i++)
                            {
                                errorMsg = emp.addRoute(deptCountry, deptTown, destCountry, destTown, deptTime, price, selectedWeek[i]);
                                resetRoute();
                                emp.loadAllRoute(table,panelModify,panelTable,labelID,comboDeptCountry1,comboDeptTown1,comboDestCountry1,
                                comboDestTown1,spinHour1,spinMinute1,spinAmPm1,txtPrice1,comboWeek);
                            }
                            if(errorMsg=="Succesfully added")
                                    JOptionPane.showMessageDialog(rootPane, errorMsg, noOfRecord+" Route added successfully", JOptionPane.PLAIN_MESSAGE);
                        }
                        else
                            errorMsg="Invalid selection, Please select weeks on the route operates";
                    }
                    else
                        errorMsg="Invalid selection, Please select a destination town";
                }
                else
                    errorMsg="Invalid selection, Please select a destination country";
            }
            else
                errorMsg="Invalid selection, Please select a departure town";
        }
        else
            errorMsg="Invalid selection, Please select a departure country";
        if((errorMsg!="")&&(errorMsg!="Succesfully added"))
            JOptionPane.showMessageDialog(rootPane, errorMsg, "Invalid Selection", JOptionPane.ERROR_MESSAGE);
        
    }//GEN-LAST:event_btnAddActionPerformed

