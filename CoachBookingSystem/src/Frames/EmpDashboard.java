    private void comboDeptCountry2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_comboDeptCountry2ActionPerformed
        // TODO add your handling code here:
        switch (comboDeptCountry2.getSelectedIndex()) {
            case 1:
                comboDestCountry2.removeAllItems();
                comboDestCountry2.addItem(" -dest country- ");
                comboDestCountry2.addItem("Spain");
                comboDestCountry2.addItem("Germany");
                break;
            case 2:
                comboDestCountry2.removeAllItems();
                comboDestCountry2.addItem(" -dest country- ");
                comboDestCountry2.addItem("Germany");
                comboDestCountry2.addItem("France");
                break;
            case 3:
                comboDestCountry2.removeAllItems();
                comboDestCountry2.addItem(" -dest country- ");
                comboDestCountry2.addItem("Spain");
                comboDestCountry2.addItem("France");
                break;
            default:
                break;
        }
        if(comboDeptCountry2.getSelectedIndex()>0)
        {
            if(searchString.length()>0)
                searchString = searchString + " AND departure_Country='"+comboDeptCountry2.getSelectedItem().toString()+"'";
            else
                searchString = searchString + " where departure_Country='"+comboDeptCountry2.getSelectedItem().toString()+"'";
        }
        
    }//GEN-LAST:event_comboDeptCountry2ActionPerformed

