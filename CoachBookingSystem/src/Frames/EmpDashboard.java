    private void btnModifyActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnModifyActionPerformed
        // TODO add your handling code here:
        int Id= Integer.parseInt(labelID.getText());
        String deptCountry = comboDeptCountry1.getSelectedItem().toString();
        String deptTown = comboDeptTown1.getSelectedItem().toString();
        String destCountry = comboDestCountry1.getSelectedItem().toString();
        String destTown = comboDestTown1.getSelectedItem().toString();
            String hour = spinHour1.getValue().toString();
            String minute = spinMinute1.getValue().toString();
            String am = spinAmPm1.getValue().toString();
        String time = hour + ":" + minute + " " + am;
        double price=0;
        try {
            price = Double.parseDouble((txtPrice1.getText()));
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(rootPane, "Price field accept only number, Please check your value", "Incorrect Input", 
                    JOptionPane.ERROR_MESSAGE);
        }
        String week = comboWeek.getSelectedItem().toString();
        
        if(!(comboDeptCountry1.getSelectedIndex()==0))
        {
            if(!(comboDeptTown1.getSelectedIndex()==0))
            {
                if(!(comboDestCountry1.getSelectedIndex()==0))
                {
                    if(!(comboDestTown1.getSelectedIndex()==0))
                    {   
                        int code;
                        
                        code = emp.modifyRoute(Id, deptCountry, deptTown, destCountry, destTown, time, price, week);
                        
                        if(code>0)
                        {
                            JOptionPane.showMessageDialog(rootPane, "Record modification successfull", "Updated Successfully", JOptionPane.INFORMATION_MESSAGE);
                            panelModify.setVisible(false);
                            emp.loadAllRoute(table,panelModify,panelTable,labelID,comboDeptCountry1,comboDeptTown1,comboDestCountry1,
                                comboDestTown1,spinHour1,spinMinute1,spinAmPm1,txtPrice1,comboWeek);
                            panelTable.setVisible(true);
                        }
                        else
                            JOptionPane.showMessageDialog(rootPane, "Record modification failed", "Update Error", JOptionPane.ERROR_MESSAGE);
                    }
                }
            }
        }
    }//GEN-LAST:event_btnModifyActionPerformed

