    private void txtPasswordKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtPasswordKeyReleased
        // TODO add your handling code here:
        checkValueTyped(txtPassword.getText().length(), lblOutPassword, lblInPassword);
        
        lblWeak.setVisible(true);
        lblStrong.setVisible(true);
        jProgressBar1.setVisible(true);
        int pLength = txtPassword.getText().length();
        if(pLength>0 && pLength<=3)
            jProgressBar1.setValue(20);
        else if(pLength>3 && pLength<=6)
            jProgressBar1.setValue(40);
        else if(pLength>6 && pLength<=9)
            jProgressBar1.setValue(60);
        else if(pLength>9 && pLength<=12)
            jProgressBar1.setValue(80);
        else if(pLength>12)
            jProgressBar1.setValue(100);
        else
            jProgressBar1.setValue(0);
    }//GEN-LAST:event_txtPasswordKeyReleased

    private void txtConfirmPasswordKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtConfirmPasswordKeyReleased
        // TODO add your handling code here:
        checkValueTyped(txtConfirmPassword.getText().length(), lblOutConPassword, lblInConPassword);
        
        String p1 = txtPassword.getText();
        String p2 = txtConfirmPassword.getText();
        
        if(p2.length()>0)
        {
            panelPassword.setVisible(true);
            if(p1.equals(p2) && !txtPassword.equals("") && !txtConfirmPassword.equals(""))
                panelPassword.setBorder(BorderFactory.createLineBorder(Color.green, 2));
            else
                panelPassword.setBorder(BorderFactory.createLineBorder(Color.red, 2));
        }
    }//GEN-LAST:event_txtConfirmPasswordKeyReleased

