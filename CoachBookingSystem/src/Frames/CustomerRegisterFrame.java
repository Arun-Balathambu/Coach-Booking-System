    private void btnSignUpActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSignUpActionPerformed
        String fName = txtFirstName.getText();
        String lName = txtSurName.getText();
        String custEmail = txtEmail.getText(); 
        String password = txtPassword.getText();
        String confirmPassword = txtConfirmPassword.getText();
        String gender;
        if(radioMale.isSelected())
            gender = "Male" ; 
        else
            gender ="Female";
        String dob = ((JTextField)dateDOB.getDateEditor().getUiComponent()).getText(); 
        String pNumber = txtPhoneNumber.getText(); 
        String address1 = txtStreet.getText(); 
        String address2 = txtCity.getText();
        
        String msg;
        
        if(!fName.isEmpty()){
            if(!lName.isEmpty()){
                if(!custEmail.isEmpty()){
                    if(!dob.isEmpty()){
                        if(!pNumber.isEmpty()){
                            if(!address1.isEmpty()){
                                if(!address2.isEmpty()){
                                    if(!password.isEmpty()){
                                        if(password.equals(confirmPassword)){
                                            Customer customer1 = new Customer(fName,lName,custEmail,password,gender,dob,pNumber,address1,address2);
                                            msg = customer1.errorMsg;
                                            if(msg.equals("Succesfully registered")){
                                                JOptionPane.showMessageDialog(rootPane, msg, "Success Message", JOptionPane.PLAIN_MESSAGE);
                                                new CustomerLoginPage().setVisible(true);
                                                this.dispose();
                                            }
                                            else
                                                msg = msg;
                                        }
                                        else
                                        msg = "passwords doesn't match make sure you have typed same password";
                                    }
                                    else
                                        msg = "Password field is empty, mandotory feilds cannot be empty";
                                }
                                else
                                    msg = "City field is empty, mandotory feilds cannot be empty";
                            }
                            else
                                msg = "Street Address field is empty, mandotory feilds cannot be empty";
                        }
                        else
                            msg = "Phone Number field is empty, mandotory feilds cannot be empty";
                    }
                    else
                        msg = "Date Of Birth field is empty, mandotory feilds cannot be empty";
                }
                else
                    msg = "email field is empty, mandotory feilds cannot be empty";
            }
            else
                msg = "Surname Name field is empty, mandotory feilds cannot be empty";
        }
        else
            msg = "First Name field is empty, mandotory feilds cannot be empty";
        
        if(!msg.equals("Succesfully registered"))
            JOptionPane.showMessageDialog(rootPane, msg, "Error message", JOptionPane.ERROR_MESSAGE);
    }//GEN-LAST:event_btnSignUpActionPerformed

