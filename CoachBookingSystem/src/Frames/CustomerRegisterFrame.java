    public void checkValueTyped(int length, javax.swing.JLabel outterLabel, javax.swing.JLabel innerLabel)
    {
        if(length == 0)
        {
            
            outterLabel.setVisible(false);
            innerLabel.setVisible(true);
        }
        else
        {   
            outterLabel.setVisible(true);
            innerLabel.setVisible(false);
        }
    }

